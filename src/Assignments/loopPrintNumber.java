package Assignments;

public class loopPrintNumber {

	public static void main(String[] args) {
		// for loop
		for(int i =10; i>=1; i--)
			System.out.println(i);
		
		System.out.println("-------------------------------------");
		
		//while loop
		int j = 10;
		while (j >= 1) {
			System.out.println(j);
			j--;
		}
		
		System.out.println("-------------------------------------");
		//do while loop
		int k=10;
		do {
			System.out.println(k);
			k--;
		} while (k >= 1);
			

	}

}
